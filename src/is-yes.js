export function isYes(ans) {
    if (ans.toLowerCase() === 'yes' || ans.toLowerCase() === 'y') {
        return true;
    } else return false;
    // console.log("hiiiiiiii")
};

export function isNo(ans) {
    if (ans.toLowerCase() === 'no' || ans.toLowerCase() === 'n') {
        return true;
    } else return false;
    // console.log("hiiiiiiii")
};